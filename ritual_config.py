#!/usr/bin/env python3
"""
Конфигурация ритуалов для таскер-бота MAX
Здесь настраиваются тексты и изображения для утренних и вечерних ритуалов
"""

import os

# Базовая директория проекта
BASE_DIR = os.path.dirname(os.path.abspath(__file__))

# Конфигурация ритуалов
RITUALS = {
    "morning": {
        "text": "Доброе утро! Как ты себя чувствуешь? Нужно настроится на продуктивный рабочий день",
        "image_path": os.path.join(BASE_DIR, "pictures", "ritual_pig.jpg"),
        "description": "Утренний ритуал для настройки на продуктивный день"
    },
    "evening": {
        "text": "Добрый вечер! Как ты себя чувствуешь? под вечер нужно расслабляться и успокаиваться",
        "image_path": os.path.join(BASE_DIR, "pictures", "ritual_pig.jpg"),
        "description": "Вечерний ритуал для расслабления и успокоения"
    }
}

# Инструкция для пользователей после вопроса о самочувствии
MOOD_INSTRUCTION = """
Оцени своё состояние от 1 до 7:

1 - Апатия (низкая энергия, нет мотивации)
2 - Пассивность (расслаблен, но пассивен)
3 - Расслабленность (спокойное состояние)
4 - Баланс (оптимальное состояние)
5 - Включенность (активность, продуктивность)
6 - Перевозбужденность (слишком много энергии)
7 - Паника (тревога, стресс)

Ответь цифрой от 1 до 7
"""

# Описания уровней самочувствия
MOOD_LEVELS = {
    1: "Апатия",
    2: "Пассивность",
    3: "Расслабленность",
    4: "Баланс",
    5: "Включенность",
    6: "Перевозбужденность",
    7: "Паника"
}


def get_ritual_config(ritual_type: str) -> dict:
    """
    Получить конфигурацию ритуала по типу
    
    Args:
        ritual_type: 'morning' или 'evening'
        
    Returns:
        Словарь с конфигурацией ритуала
    """
    return RITUALS.get(ritual_type, RITUALS["morning"])


def get_mood_description(level: int) -> str:
    """
    Получить описание уровня самочувствия
    
    Args:
        level: Уровень от 1 до 7
        
    Returns:
        Описание уровня
    """
    return MOOD_LEVELS.get(level, "Неизвестно")



